class CardComponent < ApplicationComponent
  renders_one :header
  renders_one :body

  def initialize(classes: [], html_attributes: {})
    super
  end

  renders_many :action_items, lambda { |link:|
    tag.div(link, class: "card-component__action")
  }

  def labelled_item(label, value)
    tag.dl do
      safe_join([
        tag.dt(label),
        tag.dd(value),
      ])
    end
  end

  private

  def default_classes
    %w[card-component]
  end
end
